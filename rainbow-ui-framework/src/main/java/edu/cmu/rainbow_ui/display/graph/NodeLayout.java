package edu.cmu.rainbow_ui.display.graph;

/**
 * Node layout data structure
 *
 * @author Denis Anisimov <dbanisimov@cmu.edu>
 */
public class NodeLayout {

    private int x;
    private int y;
    private int width;
    private int height;

    /**
     * Constructor with all fields initialization.
     *
     * @param x node center X coordinate
     * @param y node center Y coordinate
     * @param width node width
     * @param height node height
     */
    public NodeLayout(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }

    /**
     * @return the width
     */
    public int getWidth() {
        return width;
    }

    /**
     * @return the height
     */
    public int getHeight() {
        return height;
    }

    /**
     * @param x the x to set
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * @param y the y to set
     */
    public void setY(int y) {
        this.y = y;
    }

    /**
     * @param width the width to set
     */
    public void setWidth(int width) {
        this.width = width;
    }

    /**
     * @param height the height to set
     */
    public void setHeight(int height) {
        this.height = height;
    }
}
